package com.caio.mendes.tratamentotermico.ListarDados;

public class ListarUltimaTemp {

    private String sensor, temperatura, data;

    public String getSensor() {
        return sensor;
    }

    public void setSesor(String sensor) {
        this.sensor = sensor;
    }

    public String getTemperatura() {
        return temperatura;
    }

    public void setTemperatura(String temperatura) {
        this.temperatura = temperatura;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
